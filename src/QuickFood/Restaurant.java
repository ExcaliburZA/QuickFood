package QuickFood;

public class Restaurant {	
	//class atttributes
	public String name, location, contactNum;

	//parameterised class constructor
	public Restaurant(String name, String location, String contactNum) {
		this.name = name;
		this.location = location;
		this.contactNum = contactNum;
	}

	//default empty class constructor
	public Restaurant() {}			

}
